num1 = float(input("Введите число: "))
num2 = float(input("Введите число: "))
sign = input("Введите математический оператор: ")
if sign == "+":
    print(num1 + num2)
elif sign == "-":
    print(num1 - num2)
elif sign == "*":
    print(num1 * num2)
elif sign == "/":
    print(num1 / num2)
else:
    print("888888")